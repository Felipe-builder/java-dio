public class OperadoresRelacionais {
    public static void main(String[] args) {

        int i1 = 10;
        int i2 = 20;
        float f1 = 4.5f;
        float f2 = 5.5f;
        double d1 = 59.60;
        char c1 = 'X';
        char c2 = 'y';
        String s1 = "Fulano";
        String s2 = "Fulano";
        String s3 = "Cicrano";
        boolean b1 = true;
        boolean b2 = false;

        long l1 = 1587L;
        long l2 = 3997L;
        byte y1 = 1;
        short h1 = 25;

        System.out.println("f1 == f2 " + (f1 == f2));
        System.out.println("f1 != f2 " + (f1 != f2));
        System.out.println("f1 > f2 " + (f1 > f2));
        System.out.println("f1 < f2 " + (f1 < f2));
        System.out.println("f1 >= f2 " + (f1 >= f2));
        System.out.println("f1 >= f2 " + (f1 <= f2));

        System.out.println("f1 >= f2 " + (f1 <= f2));

    }
}
